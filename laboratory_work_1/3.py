a = float(input("Введите длину стороны равностороннего треугольника: "))

s = (a ** 2 * (3 ** 0.5)) / 4
h = (a * (3 ** 0.5)) / 2
r_in = a / (2 * (3 ** 0.5))
r_out = a / (3 ** 0.5)

print("Площадь треугольника:", s)
print("Высота треугольника:", h)
print("Радиус вписанной окружности:", r_in)
print("Радиус описанной окружности:", r_out)